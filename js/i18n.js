let currentLang = localStorage.getItem('haloula_lang') || 'ar';

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('haloula_lang', lang);
  const t = TRANSLATIONS[lang];
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

  // Update all elements with data-i18n
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) el.textContent = t[key];
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (t[key]) el.placeholder = t[key];
  });

  // Update marquee
  const marqueeAr = '✦ فساتين ✦ بلوزات ✦ بناطيل ✦ أطقم ✦ تيشيرتات ✦ جيبات ✦ أطفال ✦ شيفون';
  const marqueeEn = '✦ Dresses ✦ Tops ✦ Pants ✦ Sets ✦ T-Shirts ✦ Skirts ✦ Kids ✦ Chiffon';
  const marqueeText = lang === 'ar' ? marqueeAr : marqueeEn;
  const track = document.getElementById('marqueeTrack');
  if (track) {
    const items = (marqueeText + ' ' + marqueeText).split('✦').filter(s => s.trim()).map(s => `<span>✦ ${s.trim()}</span>`).join('');
    track.innerHTML = items;
  }

  // Update lang toggle button
  const btn = document.getElementById('langToggle');
  if (btn) btn.textContent = lang === 'ar' ? 'EN' : 'عربي';

  // Re-render products with new lang
  if (typeof renderProducts === 'function' && typeof allProducts !== 'undefined') {
    const activeBtn = document.querySelector('.filter-btn.active');
    const cat = activeBtn ? activeBtn.getAttribute('data-cat') : 'all';
    const filtered = cat === 'all' || cat === 'الكل' || cat === 'All' ? allProducts : allProducts.filter(p => p.category === cat);
    renderProducts(filtered);
    // Update filter buttons
    document.querySelectorAll('.filter-btn').forEach(btn => {
      const cat = btn.getAttribute('data-cat');
      if (cat && t['cat_' + cat]) btn.textContent = t['cat_' + cat];
      if (btn.getAttribute('data-cat') === 'all') btn.textContent = t['filter_all'];
    });
  }
}

function t(key) {
  return TRANSLATIONS[currentLang][key] || TRANSLATIONS['ar'][key] || key;
}

// Auto-apply on load
document.addEventListener('DOMContentLoaded', () => setLang(currentLang));
